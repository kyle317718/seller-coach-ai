/**
 * 랜덤 동기부여 메시지를 반환하는 함수
 * @param category - 메시지 카테고리 (선택적)
 * @returns 랜덤 동기부여 메시지
 */
export const getRandomMotivationalMessage = (category?: keyof typeof stepSpecificMessages): string => {
  if (category && stepSpecificMessages[category]) {
    const messages = stepSpecificMessages[category];
    return messages[Math.floor(Math.random() * messages.length)];
  }
  
  return generalMotivationalMessages[Math.floor(Math.random() * generalMotivationalMessages.length)];
};
